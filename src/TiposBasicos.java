
public class TiposBasicos {
    public static void main(String args[])
    {
        //TIPOS PRIMITIVOS
        int miVariableEntera = 10;
        System.out.println(miVariableEntera);
        
        miVariableEntera = 5;
        System.out.println(miVariableEntera);
        
        String miVariableCadena = "Hola";
        System.out.println(miVariableCadena);
        
        miVariableCadena = "Adios";
        System.out.println(miVariableCadena);
        
        //INFERENCIA DE TIPOS
        
        var miVariableEnteraVar = 15;
        System.out.println(miVariableEnteraVar);
        
        var miVariableCadenaVar = "Nos vemos";
        System.out.println(miVariableCadenaVar);
        
        
        
    }
}
